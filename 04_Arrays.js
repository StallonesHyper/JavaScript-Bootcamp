//Array (1-Dimensional)
var ourArray = ["Stallone", "19", "Goa"];
console.log(ourArray);

//Array (2-Dimensional)
var myArray = [["Bulls","Cows","Buffalo"],[1,2,3]];
console.log(myArray);

// Accessing Array 
var index = ourArray[0];
console.log(index)

// Modfiy Arrays With Index Positions 
ourArray[0] = "STALLONE"
console.log(ourArray[0])

// Array Updated 
// NOTE: This Will Not Work For Strings As Strings Are Immutable Functions And Cannot Be Changed Using Indexing.


// Accessing Multi-Dimensional Array 

console.log(myArray[0][1]);//Cows


// The [0] Indicates The Array Number And The [1] Indicates The Element Number Inside The Array 

