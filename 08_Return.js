function naming(name){
    let output=name;
    return output;
}

let nameOutput = naming("Stallone");
console.log(nameOutput);
//Return Statement Will Set The Value Of The Function As The Return Value


// Order Of Functions 


//Calling Before Defining
nameOutput=unnaming("STALLONE");
console.log(nameOutput);

//Defining A Function 
function unnaming(lastName){
    let output=lastName;
    return output;
}
//This Code Will Run And Execute Without Any Issues.


