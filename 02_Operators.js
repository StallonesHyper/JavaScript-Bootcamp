// GLOBAL VARIABLES 

var a = 10;
var b = 20;

// 1. Addition 
var c = a+b;
console.log(c);
console.log("Addition");


// 2. Subtraction 
var d = a-b;
console.log(d);
console.log("Subtraction");


// 3. Multiplication 
var e = a*b;
console.log(e);
console.log("Multiplication");


// 4. Division 
var f = a/b;
console.log(f);
console.log("Division");

// 5. Modulo
var g = a%b;
console.log(g);
console.log("Modulo");

//Incrementation 
var i = 10;
i++;
console.log(i);
console.log("Increment");

//Decrementation
var j = 10;
j--;
console.log(j);
console.log("Decrement");

// Hybrid Operations 
var m = 10;
m = m+5;
console.log(m);
// OR 
m += 5;
console.log(m); // Applicable For All Arithmetic Operators.


// NOTE: This Implies To Float And Double Point Numbers Also . 


// Exponent 

let expo = 2**3;
console.log(expo);
console.log("Exponential");



