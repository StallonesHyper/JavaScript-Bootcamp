// Functions Means Code On Demand 
// Which Means The Code Is Called And Executed When Needed 




//Define A Function
function funcName(name){

    //function Keyword  Function Name (Parameter){
            //Code
    //}


    // NOTE: Functions May Or May Not Have A Parameter Or Multiple VREyeParameters.

    let statement = "Hello "+ name;
    console.log(statement)


    // Also May Have A Return Statement Or Not 

}
//Calling A Function
funcName("Stallone Fernandes");


// NOTE:Functions Can Be Called Anywhere In Your Program 
// Hence Adds Flexibility 


// Example:

function add(a,b){
    var c=a+b; //These Variables Are Called Local Variables.
    console.log(c);
}

function sub(a,b){
    var c=a-b;
    console.log(c);
}
add(10,20);
sub(60,50); 


